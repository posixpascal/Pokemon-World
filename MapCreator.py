    def fileDialog(self):
        filename = QtGui.QFileDialog.getOpenFileName(self, 'Waehle eine PCF-Datei',
                    '.', 'Pokemon World Maps (world.pcf)')
        source = open(filename).read()
        u = 0
        for row in source.split("\n"):
            for job in row.split(","):
                u += 1
                self.MapContent[u]['id'] = int(job, 16)
                getattr(self, "label_%s" % (u)).setPixmap(QtGui.QPixmap("sprites/map/" + Sprites[int(job, 16)]['sprite'] + ".jpg"))
